# Run ai_comment in parallel (‡πÄ‡∏£‡∏µ‡∏¢‡∏Å‡∏à‡∏≤‡∏Å‡πÑ‡∏ü‡∏•‡πå‡∏ô‡∏µ‡πâ)
# ==================================================
async def run_generate_ai_comment_parallel(changes: List[Change]):
    """
    ‡∏¢‡∏¥‡∏á generate_ai_comment ‡∏û‡∏£‡πâ‡∏≠‡∏°‡∏Å‡∏±‡∏ô‡∏´‡∏•‡∏≤‡∏¢ Change
    - ‡∏™‡∏£‡πâ‡∏≤‡∏á semaphore ‡∏†‡∏≤‡∏¢‡πÉ‡∏ô event loop ‡πÄ‡∏î‡∏µ‡∏¢‡∏ß‡∏Å‡∏±‡∏ô (‡∏õ‡πâ‡∏≠‡∏á‡∏Å‡∏±‡∏ô error)
    """

    SEMAPHORE_LIMIT = int(os.getenv("LLM_PARALLEL_LIMIT", 8))
    semaphore = asyncio.Semaphore(SEMAPHORE_LIMIT)  # ‚úÖ ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ï‡∏£‡∏á‡∏ô‡∏µ‡πâ (‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á)

    results = await asyncio.gather(
        *[
            generate_ai_comment_async(c, semaphore, max_retries=2)
            for c in changes
        ],
        return_exceptions=True
    )

    # Debug: ‡πÅ‡∏™‡∏î‡∏á‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡πÄ‡∏Ñ‡∏™‡∏ó‡∏µ‡πà‡∏¢‡∏±‡∏á error ‡∏à‡∏£‡∏¥‡∏á ‡πÜ
    for i, r in enumerate(results):
        if isinstance(r, Exception):
            print(f"‚ö†Ô∏è [PARALLEL ERROR] Change index {i}: {r}")

